function sum(a, b, c){
    let sum = a + b + c;
    console.log(`The sum of ${a}, ${b} and ${c} is ${sum} `)
}
function avg(a, b, c){
    let avg = (a + b + c)/3;
    console.log(`The avg of ${a}, ${b} and ${c} is ${avg} `)
}

